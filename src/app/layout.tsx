import type { Metadata } from "next";
import { Cairo } from "next/font/google";
import "./globals.css";
import AllProviders from "@/shared/providers/AllProviders";
import NavBar from "@/shared/components/nav-bar";
import BackgroundImage from "@/shared/components/background-image/BackgroundImage";
import { NextIntlClientProvider } from "next-intl";

const cairo = Cairo({
  subsets: ["arabic"],
  weight: ["400", "500", "600", "700"], // 400=Regular, 500=Medium, 600=Semi-Bold, 700=Bold
  variable: "--font-cairo",
  display: "swap",
});

export async function generateStaticParams() {
  return ["en", "fr", "it", "es", "pt"].map((locale) => ({ locale }));
}

export const metadata: Metadata = {
  title: "Moonlight",
  description: "Generated by create next app",
  other: {
    preload: "/videos/HeroSection.webm",
  },
};

export default async function RootLayout({
  children,
  params,
}: Readonly<{
  children: React.ReactNode;
  params: { locale: string };
}>) {
  const { locale } = await params;

  return (
    <html lang={locale} className={`${cairo.variable} scrollbar-hide`}>
      <body className="antialiased">
        <NextIntlClientProvider>
          <AllProviders>
            <NavBar />
            {children}
            <BackgroundImage />
          </AllProviders>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
